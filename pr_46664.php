		 		<h4>The Brief</h4>
		 		<p>Dreamcatcher was appointed to execute PR for the 46664 Johannesburg fundraising concert, an initiative of the Nelson Mandela Children’s Fund.
Set for December 1, World Aids Day, and featuring a mix of local and international talent,  the concert was jointly an HIV/AIDS awareness raising  activity as well as  a fundraiser for the Children’s Fund.<br><br>
As the event publicists, our brief was to deliver PR that created awareness both about the concert and the cause it was supporting.
We were brought onto the project six weeks before the show date, thus time was of the essence, and every media intervention had to be high-impact.

</p>
<div class="spacing"></div>
		 		<h4>Our Response</h4>
		 		<p>We identified the following factors as the key communication drivers:
                <ul class="list-group">
<li>‘It’s in your hands’ - the 46664 slogan – impressed upon audiences that supporting the concert was a contribution to the fight against HIV/Aids.  </li>
<li>That the concert was an initiative by Madiba.  </li>
<li>The A-list local and international artist line-up confirmed to perform</li>
<li>This was the first 46664 concert ever to take place in South Africa</li>
</ul>

</p> 
<p>The PR action plan rolled out as follows: 
<ul class="list-group">
<li>Staggered announcements: initially, only the concert was announced; a month later the local line-up, and a week subsequently the international line-up was revealed. 
</li>
<li>Each announcement was followed by an intense round of interviews with the concert’s producers and artists. 
</li>
<li>Photo opportunities were set up on a regular basis: of the artists in private rehearsal, the build-up of the stage,  etc.
</li>
<li>Press conference and photo opportunities with the full artist line-up and Nelson Mandela in attendance two days before the concert. 
</li>
</ul>
</p>
<div class="spacing"></div>
		 		<h4>The Outcome</h4>
		 		<p>The communication programme successfully publicised the concert’s cause – raising funds for the Foundation, while rallying audiences to attend.<br><br>
The staggered media announcements worked to build anticipation, and the accompanying press conferences supported that.<br><br>
Media attendance at the show was a solid mix of local and international press across print, tv, internet and radio, while audience figures reached the target figure of 40,000. 

</p>
